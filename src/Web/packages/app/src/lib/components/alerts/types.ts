/**
 * Wrap a full <see cref="ConditionNode"/> envelope (i.e. `{type, ...payload}`)
 * coming back from the API into the editor's tree. Used by save/load paths
 * where the wire shape is a self-describing envelope rather than a side-by-
 * side `(conditionType, conditionParams)` pair — notably auto-resolve, which
 * stores a single `autoResolveParams` blob the backend deserialises directly
 * into a polymorphic <see cref="ConditionNode"/>.
 */
export function nodeFromApiEnvelope(envelope: unknown): ConditionNode | null {
	if (!envelope || typeof envelope !== "object") return null;
	const env = envelope as { type?: unknown };
	if (typeof env.type !== "string") return null;
	const kind = env.type as ConditionKind;
	const payload = (envelope as Record<string, unknown>)[kind];
	return nodeFromApi(kind, payload);
}

/**
 * Wrap a kind + opaque API payload into a `ConditionNode`. The API stores only
 * the kind-specific payload (e.g. `{direction, value}`); the FE keeps the kind
 * alongside it so a single recursive editor can dispatch on `node.type`.
 */
export function nodeFromApi(
	kind: string | undefined,
	params: unknown,
): ConditionNode | null {
	if (!kind) return null;
	if (params === null || params === undefined) return null;
	const k = kind as ConditionKind;
	const node: ConditionNode = { type: k, _uid: newUid() };
	(node as Record<string, unknown>)[k] = params;
	// Defensive: a malformed loop-staleness rule with a `<`/`<=` operator would
	// render an unselectable value in the editor (the dropdown only offers `>`
	// and `>=`). Coerce on inbound and warn so we notice if it ever happens.
	coerceStalenessOperator(node);
	// Recursively assign uids to nested nodes so keyed each-blocks have stable
	// identity for every level of the tree, not just the root.
	assignUidsRecursive(node);
	return node;
}

function coerceStalenessOperator(node: ConditionNode): void {
	const payload =
		node.type === "loop_stale"
			? node.loop_stale
			: node.type === "loop_enaction_stale"
				? node.loop_enaction_stale
				: null;
	if (!payload) return;
	const op = payload.operator as ComparisonOperator;
	if (op === "<" || op === "<=") {
		// eslint-disable-next-line no-console
		console.warn(
			`[alerts] coercing ${node.type} operator '${op}' → '>' (staleness only supports '>' / '>=')`,
		);
		payload.operator = ">";
	}
}

function assignUidsRecursive(node: ConditionNode): void {
	if (!node._uid) node._uid = newUid();
	if (node.composite?.conditions) {
		for (const child of node.composite.conditions) assignUidsRecursive(child);
	}
	if (node.not?.child) assignUidsRecursive(node.not.child);
	if (node.sustained?.child) assignUidsRecursive(node.sustained.child);
}

/**
 * Returns a deep copy of `node` with every editor-only `_uid` field stripped.
 * Use this when sending the full ConditionNode envelope to the backend (e.g.
 * `autoResolveParams`) so the editor's internal identity doesn't leak into
 * stored configuration.
 */
export function stripEditorFields(node: ConditionNode): ConditionNode {
	const cleaned: ConditionNode = { type: node.type };
	for (const k of Object.keys(node) as (keyof ConditionNode)[]) {
		if (k === "_uid" || k === "type") continue;
		const value = node[k];
		if (value === undefined) continue;
		// Recurse into nested children so uids in the subtree are also stripped.
		if (k === "composite" && node.composite) {
			cleaned.composite = {
				operator: node.composite.operator,
				conditions: node.composite.conditions.map(stripEditorFields),
			};
		} else if (k === "not" && node.not) {
			cleaned.not = { child: stripEditorFields(node.not.child) };
		} else if (k === "sustained" && node.sustained) {
			cleaned.sustained = {
				minutes: node.sustained.minutes,
				child: stripEditorFields(node.sustained.child),
			};
		} else {
			(cleaned as Record<string, unknown>)[k] = value;
		}
	}
	return cleaned;
}

/**
 * Extract the kind-specific payload for the API. Returns `null` when there's
 * no node.
 */
export function nodeToApi(
	node: ConditionNode | null,
): { conditionType: string; conditionParams: unknown } | null {
	if (!node) return null;
	const params = (node as Record<string, unknown>)[node.type];
	return { conditionType: node.type, conditionParams: params ?? {} };
}

/**
 * Serialise the editor's {@link RuleEditorState} into the wire-format body
 * shared by {@link CreateAlertRuleRequest} and {@link UpdateAlertRuleRequest}.
 *
 * Strips `_uid` markers and flattens single-child composite roots so the output
 * is identical for any two semantically-equal editor states regardless of their
 * internal identity markers. Use this both for the actual save call and for
 * dirty detection (compare `JSON.stringify` of current vs. saved body).
 */
export function buildBody(state: RuleEditorState) {
	const flat = flattenSingleChildRoot(state.condition!);
	const api = nodeToApi(flat);
	return {
		name: state.name,
		description: state.description || undefined,
		conditionType: api?.conditionType as AlertConditionType,
		conditionParams: api?.conditionParams,
		isEnabled: state.isEnabled,
		sortOrder: state.sortOrder,
		severity: state.severity,
		allowThroughDnd: state.allowThroughDnd,
		autoResolveEnabled: state.autoResolveEnabled,
		autoResolveParams: state.autoResolveCondition
			? stripEditorFields(flattenSingleChildRoot(state.autoResolveCondition))
			: undefined,
		clientConfiguration: {
			...state.clientConfig,
			snooze: {
				...state.clientConfig.snooze,
				conditions: state.clientConfig.snooze.conditions.map((c) =>
					stripEditorFields(flattenSingleChildRoot(c))
				),
			},
		},
		channels: state.channels.map((c) => ({
			channelType: c.channelType,
			destination: c.destination || undefined,
			destinationLabel: c.destinationLabel || undefined,
		})),
	};
}

