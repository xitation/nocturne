/**
 * The set of valid {@link ConditionNode.type} values. Mirrors the backend
 * <c>AlertConditionType</c> enum — kebab-/snake-case strings.
 */
export type ConditionKind = `${AlertConditionType}`;

export type ComparisonOperator = ">=" | ">" | "<=" | "<";

/**
 * Staleness is conceptually directional ("more than N minutes since…"), so the
 * loop-staleness kinds only accept `>` and `>=`. The wider {@link StalenessCondition}
 * (BG staleness) was shipped accepting all four operators and predates this
 * narrowing; widening would break stored rules using `<` for auto-resolve.
 */
export type StalenessOperator = ">" | ">=";

export type TrendBucket =
	| "falling_fast"
	| "falling"
	| "flat"
	| "rising"
	| "rising_fast";

/**
 * Composite condition (AND/OR group). The wire-side child list points back at
 * the editor's {@link ConditionNode}, not the bare generated record, so the
 * `_uid` extension propagates through nested groups.
 */
export interface CompositePayload {
	operator: "and" | "or";
	conditions: ConditionNode[];
}

export interface NotPayload {
	child: ConditionNode;
}

export interface SustainedPayload {
	minutes: number;
	child: ConditionNode;
}

/**
 * The editor's recursive condition tree. Field shapes for leaf nodes come
 * straight from the generated client; the only editor-only addition is `_uid`,
 * stripped before the tree is sent back over the wire (see
 * {@link stripEditorFields}).
 *
 * `composite`/`not`/`sustained` use editor-aware payloads here so the `_uid`
 * extension propagates through nested groups — the generated wire types refer
 * to a bare `ConditionNode` without it.
 */
export interface ConditionNode {
	type: ConditionKind;
	/**
	 * Editor-only stable identity for keyed `{#each}` blocks. Set on
	 * construction (`defaultPayload`/`nodeFromApi`); stripped before the node
	 * is sent over the wire by {@link stripEditorFields}. Index-based keys
	 * cause Svelte 5 to re-bind the wrong nested editor instance after a
	 * sibling is removed; this avoids that.
	 */
	_uid?: string;
	composite?: CompositePayload;
	not?: NotPayload;
	sustained?: SustainedPayload;
	threshold?: ThresholdCondition;
	rate_of_change?: RateOfChangeCondition;
	signal_loss?: SignalLossCondition;
	staleness?: StalenessCondition;
	predicted?: PredictedCondition;
	trend?: TrendCondition;
	time_of_day?: TimeOfDayCondition;
	iob?: IobCondition;
	cob?: CobCondition;
	reservoir?: ReservoirCondition;
	site_age?: SiteAgeCondition;
	sensor_age?: SensorAgeCondition;
	alert_state?: AlertStateCondition;
	loop_stale?: LoopStaleCondition;
	loop_enaction_stale?: LoopEnactionStaleCondition;
	pump_suspended?: PumpSuspendedCondition;
	pump_battery?: PumpBatteryCondition;
	temp_basal?: TempBasalCondition;
	uploader_battery?: UploaderBatteryCondition;
	override_active?: OverrideActiveCondition;
	sensitivity_ratio?: SensitivityRatioCondition;
	do_not_disturb?: DoNotDisturbCondition;
	glucose_bucket?: GlucoseBucketCondition;
	time_since_last_carb?: TimeSinceLastCarbCondition;
	time_since_last_bolus?: TimeSinceLastBolusCondition;
	day_of_week?: DayOfWeekCondition;
	pump_state?: PumpStateCondition;
	state_span_active?: StateSpanActiveCondition;
}

function newUid(): string {
	if (typeof crypto !== "undefined" && "randomUUID" in crypto) {
		return crypto.randomUUID();
	}
	return Math.random().toString(36).slice(2);
}

/**
 * Wrap a non-composite node in a single-child AND group, leaving composite
 * nodes untouched. The inline rule builder always edits at the group level —
 * single-leaf rules are still rendered as one IF row inside an implicit AND.
 * The save path (caller-side) flattens single-child composites back to the
 * inner node so the wire shape doesn't grow a redundant wrapper.
 */
export function ensureCompositeRoot(node: ConditionNode): ConditionNode {
	if (node.type === "composite") return node;
	return {
		type: "composite",
		_uid: newUid(),
		composite: { operator: "and", conditions: [node] },
	};
}

/**
 * Inverse of {@link ensureCompositeRoot}. Returns the inner node when the root
 * is a composite holding exactly one child; otherwise returns the input
 * unchanged. Use this when serialising the editor's tree back to the API so
 * single-leaf rules stay flat on the wire.
 */
export function flattenSingleChildRoot(node: ConditionNode): ConditionNode {
	if (
		node.type === "composite" &&
		node.composite &&
		node.composite.conditions.length === 1
	) {
		return node.composite.conditions[0];
	}
	return node;
}

